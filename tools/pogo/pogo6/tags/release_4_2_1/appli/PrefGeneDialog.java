  public int showDialog(String path, boolean is_superclass)
  {
     //  Initialize Window with input parameters
    //-----------------------------------------------
 	if (is_superclass)
	{
		asSuperClassBtn.setSelected(true);
		asDeviceServerBtn.setSelected(false);
	}
	else
	{
		asSuperClassBtn.setSelected(false);
		asDeviceServerBtn.setSelected(true);
	}

	outPathText.setText(path);
	outPathText.setRequestFocusEnabled(true);
	outPathText.requestFocus();
	setVisible(true);
	return returnStatus;
  }
//======================================================
//======================================================
  private void doClose(int retStatus)
  {
    returnStatus = retStatus;
    setVisible (false);
    dispose ();
  }

